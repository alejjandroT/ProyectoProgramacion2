    private Ciudad buscarCiudadPorNombre(String nombre) {
        if (nombre == null) {
            return null;
        }
        List<Ciudad> ciudades = ciudadService.listarCiudades();
        for (Ciudad ciudad : ciudades) {
            if (ciudad.getNombre().equals(nombre)) {
                return ciudad;
            }
        }
        return null;
    }

