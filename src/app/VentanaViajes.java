    private void tablaViajesMouseClicked(MouseEvent evt) {
        int selectedRow = tablaViajes.getSelectedRow();
        if (selectedRow >= 0) {
            String idConductor = tablaViajes.getValueAt(selectedRow, 1).toString();
            String placaVehiculo = tablaViajes.getValueAt(selectedRow, 2).toString();
            String origenNombre = tablaViajes.getValueAt(selectedRow, 3).toString();
            String destinoNombre = tablaViajes.getValueAt(selectedRow, 4).toString();

            List<Conductor> conductores = viajeControlador.getViajeService().getConductorService().listarConductores();
            for (Conductor conductor : conductores) {
                String nombreCompleto = conductor.getNombre() + " " + conductor.getApellido();
                if (conductor.getIdentificacion().equals(idConductor)) {
                    cbConductor.setSelectedItem(nombreCompleto);
                    break;
                }
            }
            cbComboVehiculo.setSelectedItem(placaVehiculo);

            List<Ciudad> ciudades = viajeControlador.getCiudadService().listarCiudades();
            for (Ciudad ciudad : ciudades) {
                if (ciudad.getNombre().equals(origenNombre)) {
                    cbOrigen.setSelectedItem(ciudad);
                }
                if (ciudad.getNombre().equals(destinoNombre)) {
                    cbOrigen.setSelectedItem(ciudad);
                }
            }
        }
    }

