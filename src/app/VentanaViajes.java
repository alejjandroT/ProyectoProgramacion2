    private void btnDetenerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetenerActionPerformed
        int selectedRow = tablaViajes.getSelectedRow();
        if (selectedRow >= 0) {
            String id = tablaViajes.getValueAt(selectedRow, 0).toString();
            try {
                viajeControlador.detenerViaje(id);
                JOptionPane.showMessageDialog(this, "Viaje detenido correctamente.");
            } catch (IllegalStateException | IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un viaje para detener.");
        }
    }//GEN-LAST:event_btnDetenerActionPerformed

    private void btnModificarViaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarViaActionPerformed
        int selectedRow = tablaViajes.getSelectedRow();
        if (selectedRow >= 0) {
            String id = tablaViajes.getValueAt(selectedRow, 0).toString();
            String nombreConductor = (String) cbConductor.getSelectedItem();
            String placaVehiculo = (String) cbComboVehiculo.getSelectedItem();

            // Obtener los objetos Ciudad desde los nombres seleccionados
            Ciudad origen = buscarCiudadPorNombre((String) cbOrigen.getSelectedItem());
            Ciudad destino = buscarCiudadPorNombre((String) cbDestino.getSelectedItem());

            if (nombreConductor == null || placaVehiculo == null || origen == null || destino == null) {
                JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
                return;
            }

            try {
                viajeControlador.modificarViaje(id, nombreConductor, placaVehiculo, origen, destino);
                JOptionPane.showMessageDialog(this, "Viaje modificado correctamente.");
                limpiarCampos();
            } catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un viaje para modificar.");
        }
    }//GEN-LAST:event_btnModificarViaActionPerformed

    private void btnCancelarViajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarViajActionPerformed
        int selectedRow = tablaViajes.getSelectedRow();
        if (selectedRow >= 0) {
            String id = tablaViajes.getValueAt(selectedRow, 0).toString();
            viajeControlador.eliminarViaje(id);
            JOptionPane.showMessageDialog(this, "Viaje cancelado correctamente.");
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un viaje para cancelar.");
        }
    }//GEN-LAST:event_btnCancelarViajActionPerformed
    private void limpiarCampos() {
        cbConductor.setSelectedIndex(-1);
        cbComboVehiculo.setSelectedIndex(-1);
        cbOrigen.setSelectedIndex(-1);
        cbDestino.setSelectedIndex(-1);
        tablaViajes.clearSelection();
        viajeControlador.actualizarTabla();
    }

